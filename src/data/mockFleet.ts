export const getFleetStats = (fleet: Aircraft[]) => ({
  total: fleet.length,
  healthy: fleet.filter(a => a.status === 'healthy').length,
  warning: fleet.filter(a => a.status === 'warning').length,
  critical: fleet.filter(a => a.status === 'critical').length,
  maintenance: fleet.filter(a => a.status === 'maintenance').length,
  avgHealth: Math.round(fleet.reduce((s, a) => s + a.healthScore, 0) / fleet.length),
});
