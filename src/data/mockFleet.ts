import type { Aircraft, Engine } from '../types';

const engineTypes = ['CFM56-7B', 'GE90-115B', 'PW4000', 'Trent XWB', 'LEAP-1B', 'CF6-80'];

function makeEngine(idx: number, health: number): Engine {
  const statuses = health > 80 ? 'healthy' : health > 60 ? 'warning' : 'critical';
  return {
    id: `ENG-${idx}`,
    name: `Engine ${idx === 1 ? 'Left' : idx === 2 ? 'Right' : idx === 3 ? 'APU' : `#${idx}`}`,
    type: engineTypes[Math.floor(Math.random() * engineTypes.length)],
    healthScore: health,
    status: statuses as Engine['status'],
    lastInspection: `2026-0${Math.floor(Math.random() * 6) + 1}-${String(Math.floor(Math.random() * 28) + 1).padStart(2, '0')}`,
    hoursRemaining: Math.floor(health * 12 + Math.random() * 200),
  };
}

